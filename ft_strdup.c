#include "libft.h"

char	*ft_strdup(const char *str)
{
	char	*new;
	int		i;
	int		size;

	size = 0;
	while (str[size])
		++size;
	new = malloc(sizeof(char) * (size + 1));
	if (!new)
		return (NULL);
	i = 0;
	while (str[i])
	{
		new[i] = str[i];
		i++;
	}
	new[i] = '\0';
	return (new);
}
